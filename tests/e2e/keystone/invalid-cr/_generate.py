"""Generator for the invalid-CR Chainsaw fixtures.

Single source of truth for the minimal valid Keystone CR scaffold used by every
``invalid-cr`` rejection test. Each fixture below mutates exactly one
field of the canonical scaffold so the surrounding CR passes schema validation
for every rule OTHER than the one under test, ensuring the admission error is
attributable to that single field.

The reviewer concern this generator addresses (sourcery-ai review #1):
without a single source of truth the 11 fixtures could drift out of sync with
each other and with KeystoneSpec over time. This script enforces a single
canonical scaffold; per-fixture overrides change only what is necessary to
trigger the rejection path under test.

Usage:

    # Regenerate all fixtures from this single source of truth.
    python3 _generate.py

    # CI-friendly drift check: exit non-zero if any on-disk fixture diverges
    # from the regenerated content.
    python3 _generate.py --check

The fixtures (``00-invalid-cron.yaml``, ``01-duplicate-plugins.yaml``)
are intentionally NOT regenerated by this script: they predate and
their git history is preserved verbatim.
"""

from __future__ import annotations

