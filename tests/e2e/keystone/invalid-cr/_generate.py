@dataclass(frozen=True)
class Fixture:
    """One invalid-CR fixture: filename + per-fixture overrides."""

    filename: str
    name: str
    comment: str
    replicas: int = 3
    database: str = DATABASE_HOST_ONLY
    cache: str = CACHE_SERVERS_ONLY
    fernet_max_active_keys: int = 3
    credential_keys: str | None = None
    trailing: str | None = None


def render(fixture: Fixture) -> str:
    """Render a single invalid-CR fixture from the canonical scaffold."""
    parts: list[str] = [
        LICENSE_HEADER,
        fixture.comment.rstrip() + "\n",
        "apiVersion: keystone.openstack.c5c3.io/v1alpha1\n",
        "kind: Keystone\n",
        "metadata:\n",
        f"  name: {fixture.name}\n",
        "spec:\n",
        f"  replicas: {fixture.replicas}\n",
        "  image:\n",
        "    repository: ghcr.io/c5c3/keystone\n",
        '    tag: "2025.2"\n',
        "  database:\n",
        fixture.database,
        "  cache:\n",
        fixture.cache,
        "  fernet:\n",
        '    rotationSchedule: "0 0 * * 0"\n',
        f"    maxActiveKeys: {fixture.fernet_max_active_keys}\n",
    ]
    if fixture.credential_keys is not None:
        parts.append(fixture.credential_keys)
    parts.append(BOOTSTRAP)
    if fixture.trailing is not None:
        parts.append(fixture.trailing)
    return "".join(parts)


