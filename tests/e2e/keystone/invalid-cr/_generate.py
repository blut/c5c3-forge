def main(argv: list[str]) -> int:
    check_only = "--check" in argv
    here = Path(__file__).resolve().parent
    drift: list[str] = []
    for fixture in FIXTURES:
        rendered = render(fixture)
        target = here / fixture.filename
        # (I-001): pin encoding=utf-8 so the em-dash characters in the
        # rendered comments round-trip deterministically on runners with a
        # non-UTF-8 locale; otherwise locale.getpreferredencoding() could
        # mis-decode the on-disk file and falsely flag every fixture as drifted.
        if target.exists() and target.read_text(encoding="utf-8") == rendered:
            continue
        if check_only:
            drift.append(fixture.filename)
        else:
            target.write_text(rendered, encoding="utf-8")
            print(f"wrote {fixture.filename}")
    # (W-002): orphan sweep. Without this, removing a Fixture entry
    # without `git rm`'ing the YAML leaves dead code on disk that --check
    # cannot see (it only iterates FIXTURES). Report orphans as drift in
    # check mode; delete them otherwise so the on-disk set matches FIXTURES.
    # Files in _PRE_CC0094_FIXTURES are exempted because they predate
    # and are not generated here.
    expected = {f.filename for f in FIXTURES} | _PRE_CC0094_FIXTURES
    for path in sorted(here.iterdir()):
        if (
            path.is_file()
            and _FIXTURE_FILENAME_PATTERN.match(path.name)
            and path.name not in expected
        ):
            if check_only:
                drift.append(f"{path.name} (orphan: not in FIXTURES)")
            else:
                path.unlink()
                print(f"removed orphan {path.name}")
    if check_only and drift:
        print(
            "drift detected — re-run `python3 _generate.py` to refresh:",
            file=sys.stderr,
        )
        for filename in drift:
            print(f"  {filename}", file=sys.stderr)
        return 1
    return 0


if __name__ == "__main__":
    sys.exit(main(sys.argv[1:]))
