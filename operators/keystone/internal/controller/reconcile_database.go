// dbJobUIDAnnotationFormat builds the annotation key used to dedupe terminal
// metric emission for a given DB-related Job. The annotation lives on the
// Keystone CR so it survives Job deletion (e.g. when RunJob recreates the Job
// after a PodSpec hash change) and the operator can still skip re-emission
// when the same UID is observed twice (CC-0089, REQ-005).
//
// The %s placeholder is the buildDBJob nameSuffix ("db-sync", "db-expand",
// "db-migrate", "db-contract", "schema-check"). Each phase keeps an
// independent dedupe annotation so metric emission for the expand-migrate-
// contract upgrade Jobs and the post-sync schema-check Job is never silently
// suppressed by an unrelated phase having already been observed.
const dbJobUIDAnnotationFormat = "forge.c5c3.io/last-%s-job-uid"

// dbJobUIDAnnotationKey returns the dedupe annotation key for the Job
// identified by the given nameSuffix (see dbJobUIDAnnotationFormat).
func dbJobUIDAnnotationKey(jobSuffix string) string {
	return fmt.Sprintf(dbJobUIDAnnotationFormat, jobSuffix)
}

// Feature: CC-0013

// mariaDBResourceCtors is the single source of truth for the set of MariaDB CR
// kinds owned by a Keystone CR. Both the finalizer cleanup
// (finalizeDatabaseResources) and the FinalizingDatabase sentinel
// (hasLiveMariaDBResources) iterate over this list, so adding a new kind (e.g.
// Backup) only requires appending one entry here — the cleanup and the
// sentinel then stay in lockstep. Each ctor returns a zeroed pointer suitable
// for Get/Delete; callers set Name and Namespace via mariaDBResourceKey
// (CC-0078, REQ-002, REQ-007).
var mariaDBResourceCtors = []func() client.Object{
	func() client.Object { return &mariadbv1alpha1.Database{} },
	func() client.Object { return &mariadbv1alpha1.User{} },
	func() client.Object { return &mariadbv1alpha1.Grant{} },
}

// mariaDBResourceKey returns the client.ObjectKey used for the MariaDB
// Database, User, and Grant CRs owned by keystone. Keeping the naming
// convention (keystone.Name, keystone.Namespace) in one place ensures that
// buildDatabase, buildUser, buildGrant, hasLiveMariaDBResources, and
// finalizeDatabaseResources never disagree on the key — a future name suffix
// or namespace override changes here and propagates to every call site
// (CC-0078, REQ-002).
func mariaDBResourceKey(keystone *keystonev1alpha1.Keystone) client.ObjectKey {
	return client.ObjectKey{Name: keystone.Name, Namespace: keystone.Namespace}
}

